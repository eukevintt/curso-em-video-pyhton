print('- Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a média -')

n1 = float(input('Primeira nota do aluno: '))
n2 = float(input('Segunda nota do aluno: '))
media = (n1 + n2) / 2

print('As notas do aluno respectivamente foram {} e {} e a média foi {}'.format(n1, n2, media))